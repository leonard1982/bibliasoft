   function lookup_otro(&$otro) 
   {
      $conteudo = "" ; 
      if ($otro == "1")
      { 
          $conteudo = "SI";
      } 
      if ($otro == "0")
      { 
          $conteudo = "NO";
      } 
      if (!empty($conteudo)) 
      { 
          $otro = $conteudo; 
      } 
   }  
