      }
      if ($_SESSION['sc_session'][$this->Ini->sc_page]['grid_ventas']['SC_Ind_Groupby'] == "vencimiento")
      {
          $this->sum_total = $_SESSION['sc_session'][$this->Ini->sc_page]['grid_ventas']['tot_geral'][2];
      }
      if ($_SESSION['sc_session'][$this->Ini->sc_page]['grid_ventas']['SC_Ind_Groupby'] == "credito")
      {
          $this->sum_total = $_SESSION['sc_session'][$this->Ini->sc_page]['grid_ventas']['tot_geral'][2];
      }
      if ($_SESSION['sc_session'][$this->Ini->sc_page]['grid_ventas']['SC_Ind_Groupby'] == "vendedor")
