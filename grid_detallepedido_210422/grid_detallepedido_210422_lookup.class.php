   function lookup_estado_comanda(&$estado_comanda) 
   {
      $conteudo = "" ; 
      if ($estado_comanda == "1")
      { 
          $conteudo = "PENDIENTE";
      } 
      if ($estado_comanda == "2")
      { 
          $conteudo = "PROCESO";
      } 
      if ($estado_comanda == "3")
      { 
          $conteudo = "CERRADA";
      } 
      if (!empty($conteudo)) 
      { 
          $estado_comanda = $conteudo; 
      } 
