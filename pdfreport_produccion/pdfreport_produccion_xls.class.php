          {
              $this->numero = substr($this->numero, 0, $tmp_pos);
          }
          $this->numero_2 = $Busca_temp['numero_input_2']; 
          $this->fecha = $Busca_temp['fecha']; 
          $tmp_pos = strpos($this->fecha, "##@@");
          if ($tmp_pos !== false && !is_array($this->fecha))
