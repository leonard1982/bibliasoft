          $this->Zip_f = $this->Ini->root . $this->Ini->path_imag_temp . "/" . $this->Arq_zip;
      }
      $this->arr_export = array('label' => array(), 'lines' => array());
      $this->arr_span   = array();

      if (isset($_SESSION['sc_session'][$this->Ini->sc_page]['pdfreport_produccion']['embutida_label']) && $_SESSION['sc_session'][$this->Ini->sc_page]['pdfreport_produccion']['embutida_label'])
